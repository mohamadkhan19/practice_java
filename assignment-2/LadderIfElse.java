
public class LadderIfElse {
	public static void main(String args[])
	{
		int i= Integer.parseInt(args[0]);
		if(i==1)
			System.out.println("Hello");
		else if(i==2)
			System.out.println("hi");
		else if(i==3)
			System.out.println("Good");
		else
			System.out.println("No match found");
		
	}

}
